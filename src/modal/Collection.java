package modal;

public class Collection {
	private String collection;
	private String volumn;
	private String volumn_change;
	private String floor_price;
	private String price_change;

	public String getCollection() {
		return this.collection;
	}

	public void setCollection(String collection) {
		this.collection = collection;
	}

	public String getVolumn() {
		return this.volumn;
	}

	public void setVolumn(String volumn) {
		this.volumn = volumn;
	}

	public String getVolumn_change() {
		return this.volumn_change;
	}

	public void setVolumn_change(String volumn_change) {
		this.volumn_change = volumn_change;
	}

	public String getFloor_price() {
		return this.floor_price;
	}

	public void setFloor_price(String floor_price) {
		this.floor_price = floor_price;
	}

	public String getPrice_change() {
		return this.price_change;
	}

	public void setPrice_change(String price_change) {
		this.price_change = price_change;
	}

}
